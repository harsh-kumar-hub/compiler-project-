int main(){
	int choice;
    choice = 2;
    print "Switch case result:\n";
    switch (choice) {
        case 1:
            print "One\n";
            break;
        case 2:
            print "Two\n";
            break;
        case 3:
            print "Three\n";
            break;
        default:
            print "Other\n";
            break;
    }
}